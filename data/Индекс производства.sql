    metallurgy NUMERIC(5,1),               -- Металлургическое производство и производство готовых металлических изделий
    machinery NUMERIC(5,1),                -- Производство машин и оборудования
    electrical NUMERIC(5,1),               -- Производство электрооборудования, электронного и оптического оборудования
    transport NUMERIC(5,1),                -- Производство транспортных средств и оборудования
    other_manufacturing NUMERIC(5,1),      -- Прочие производства
    energy_water NUMERIC(5,1)              -- Производство и распределение электроэнергии, газа и воды
);

